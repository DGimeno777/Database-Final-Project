function distance($lat1, $lon1, $lat2, $lon2, $unit) {

  $theta = $lon1 - $lon2;
  $dist = sin(deg2rad($lat1)) * sin(deg2rad($lat2)) +  cos(deg2rad($lat1)) * cos(deg2rad($lat2)) * cos(deg2rad($theta));
  $dist = acos($dist);
  $dist = rad2deg($dist);
  $miles = $dist * 60 * 1.1515;
  $unit = strtoupper($unit);

  if ($unit == "K") {
    return ($miles * 1.609344);
  } else if ($unit == "N") {
      return ($miles * 0.8684);
    } else {
        return $miles;
      }
}

function get_lat_and_long($user1ID, $user2ID){
    global $db;
    $query = "Call get_lat_and_long('$user1ID', '$user2ID')";
    $query = $db->query($query);
    return $query;
}

function display_entry_based_on_location($user1ID, $user2ID, $radius) {
    $locs = get_lat_and_long($user1ID, $user2ID);
    $locs = $locs->fetch();
    
    $distance = distance($locs["user1_lat"], $locs["user1_long"], $locs["user2_lat"], $locs["user2_long"], 'M');
        
    return $distance < $radius;
}

